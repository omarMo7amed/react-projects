const average = (arr) =>
  arr.reduce((acc, cur, i, arr) => acc + cur / arr.length, 0);
const KEY = "b4a9ddd2";

export default function App() {
  const [query, setQuery] = useState("");
  const { movies, error, isLoading } = useMovies(query);
  const [selectedMovie, setSelectedMovie] = useState(null);
  // i don't know how it use but this use to cancel previous request to reduce failed
  const [watched, setWatched] = useLocalStorageState([], "watched");
  //if we fetch api without useEffect , then infinite request occur
  function handleSelectedMovie(id) {
    setSelectedMovie((selectedMovie) => (selectedMovie === id ? null : id));
  }
  function handleCloseMovie() {
    setSelectedMovie(null);
  }
  function handleAddWatched(movie) {
    setWatched((watched) => [...watched, movie]);
  }
  function handleDeleteWatched(id) {
    setWatched((watched) => watched.filter((movie) => movie.imdbID !== id));
  }

  return (
    <>
      <NavBar>
        <Logo />
        <Search query={query} setQuery={setQuery} />
        <NumResult movies={movies} />
      </NavBar>
      <Main>
        <Box>
          {/*{isLoading ? <Loader /> : <MovieList movies={movies} />}*/}
          {isLoading && <Loader />}
          {!isLoading && !error && (
            <MovieList
              movies={movies}
              onSelectedMovie={handleSelectedMovie}
              setWatched={setWatched}
            />
          )}
          {error && <ErrorMessage message={error} />}
        </Box>
        <Box>
          {selectedMovie ? (
            <MovieDetails
              selectedMovie={selectedMovie}
              onCloseMovie={handleCloseMovie}
              onAddWatched={handleAddWatched}
              watched={watched}
            />
          ) : (
            <>
              <WatchedMoviesList
                watched={watched}
                onDeleteWatched={handleDeleteWatched}
              />
              <WatchedSummary watched={watched} />
            </>
          )}
        </Box>

        {/*
        the same way but this used in React-router
        <Box element={<MovieList movies={movies}}/>
        <Box element={<>
          <WatchedSummary watched={watched} />
          <WatchedMoviesList watched={watched} />
          </>
        }/>
      */}
      </Main>
    </>
  );
}

